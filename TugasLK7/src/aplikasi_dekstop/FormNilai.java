    private void ubahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ubahActionPerformed
        // TODO add your handling code here:
        try {
            Double nilaiAkhir;
            
            Double ulanganHarian = parseDouble(inputUH.getText());
            Double ulanganTengah = parseDouble(inputUTS.getText());
            Double ulanganAkhir = parseDouble(inputUAS.getText());
            
            nilaiAkhir = (ulanganHarian + ulanganTengah + ulanganAkhir) / 3;
 
            PreparedStatement ps = conn.prepareStatement("UPDATE nilai SET  kodejurusan = ?, kodekelas = ?, idmapel = ?, nilaisiswa = ? WHERE nis = ?");
           
            ps.setString(1, inputJurusan.getText());
            ps.setString(2, inputKelas.getText());
            ps.setString(3, inputMapel.getText());
            ps.setString(4, String.valueOf(nilaiAkhir));
            ps.setString(5, inputNis.getText());
            
            ps.executeUpdate();

            refreshTable();
            inputNis.setText("");
            inputJurusan.setText("");
            inputKelas.setText("");
            inputMapel.setText("");
            inputUH.setText("");
            inputUTS.setText("");
            inputUAS.setText("");
            

        } catch (Exception e) {
            System.out.print("ERROR KUERI KE DATABASE:\n" + e + "\n\n");
        }
    }//GEN-LAST:event_ubahActionPerformed

    private void tambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tambahActionPerformed
        // TODO add your handling code here:
        try {
            Double nilaiAkhir;
            
            Double ulanganHarian = parseDouble(inputUH.getText());
            Double ulanganTengah = parseDouble(inputUTS.getText());
            Double ulanganAkhir = parseDouble(inputUAS.getText());
            
            nilaiAkhir = (ulanganHarian + ulanganTengah + ulanganAkhir) / 3;
            
            PreparedStatement ps = conn.prepareStatement("INSERT INTO nilai (nis, kodejurusan, kodekelas, idmapel, nilaisiswa) VALUE (?,?,?,?,?)");
            
            ps.setString(1, inputNis.getText());
            ps.setString(2, inputJurusan.getText());
            ps.setString(3, inputKelas.getText());
            ps.setString(4, inputMapel.getText());
            ps.setString(5, String.valueOf(df2.format(nilaiAkhir)));
            ps.executeUpdate();

            refreshTable();
            inputNis.setText("");
            inputJurusan.setText("");
            inputKelas.setText("");
            inputMapel.setText("");
            inputUH.setText("");
            inputUTS.setText("");
            inputUAS.setText("");

        } catch (Exception e) {
            System.out.print("ERROR KUERI KE DATABASE:\n" + e + "\n\n");
        }
    }//GEN-LAST:event_tambahActionPerformed

