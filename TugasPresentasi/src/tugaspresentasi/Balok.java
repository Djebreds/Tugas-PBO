    private class UntukBalok extends Balok {
        double hitungLuas() {
            Double panjang, lebar, luas, tinggi;
            
            panjang = Double.parseDouble(inputPanjang.getText());
            lebar = Double.parseDouble(inputLebar.getText());
            tinggi = Double.parseDouble(inputTinggi.getText());
            luas = 2 * ((panjang * lebar) + (lebar * tinggi) + (panjang * tinggi));
            
            return luas;
        }
        
        double hitungKeliling() {
            Double panjang, lebar, tinggi, keliling;
            panjang = Double.parseDouble(inputPanjang.getText());
            lebar = Double.parseDouble(inputLebar.getText());
            tinggi = Double.parseDouble(inputTinggi.getText());
            keliling = 4 * (panjang + lebar + tinggi);
            
            return keliling;
        }
        
        
    }
    
    private class Persegi extends Balok {

          double hitungLuas() {
            Double panjang, lebar, luas;
            panjang = Double.parseDouble(inputPanjang.getText());
            lebar = Double.parseDouble(inputLebar.getText());
            luas = panjang * lebar;
            
            return luas;
        }
        
        double hitungKeliling() {
            Double panjang, lebar, keliling;
            panjang = Double.parseDouble(inputPanjang.getText());
            lebar = Double.parseDouble(inputLebar.getText());
            keliling = (2 * panjang) + (2 * lebar);
            
            return keliling;
        }
    }
