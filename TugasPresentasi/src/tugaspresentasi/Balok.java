    private void inputPilihanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_inputPilihanActionPerformed
        // TODO add your handling code here:
        Persegi kotak = new Persegi();
        UntukBalok balok = new UntukBalok();
        
        String pilihan;
        
        Double luas1, keliling1, luas2, keliling2;
        pilihan = (String)inputPilihan.getSelectedItem();
        
        if (inputRadio1.isSelected()) {
            switch(pilihan) {
                case "Luas" :
                    luas1 = kotak.hitungLuas();
                    hasil.setText("Luas Persegi Panjang : " + String.valueOf(luas1));
                    break;
                case "Keliling" :
                    keliling1 = kotak.hitungKeliling();
                    hasil.setText("keliling Persegi Panjang : " + String.valueOf(keliling1));
                    break;
            }
        }
        
        if (inputRadio2.isSelected()) {
            switch(pilihan) {
                case "Luas" :
                    luas2 = balok.hitungLuas();
                    hasil.setText("Luas Balok : " + String.valueOf(luas2));
                    break;
                case "Keliling" :
                    keliling2 = balok.hitungKeliling();
                    hasil.setText("Keliling Balok : " + String.valueOf(keliling2));
                    break;
             }
        }
        
    }//GEN-LAST:event_inputPilihanActionPerformed

