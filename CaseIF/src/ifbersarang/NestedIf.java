public class NestedIf {
    public static void main(String[] args) {
        String kata;
        kata = "Bandung";
        
        if ("Bandung".equals(kata)) {
            System.out.println(kata + " adalah ibu kota Jawa Barat");
        } else {
            System.out.println(kata);
        }
        
    }
}
