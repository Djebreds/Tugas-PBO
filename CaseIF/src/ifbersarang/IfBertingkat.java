public class IfBertingkat {
    public static void main(String[] args) {
        String kota = "Jakarta";
        
        if (kota == "Jakarta") {
            System.out.println(kota + " adalah Ibu Kota Indonesia");
        } else if (kota == "Bandung") {
            System.out.println(kota + " adalah Ibu Kota Jawa Barat");
        } else if (kota == "Semarang") {
            System.out.println(kota + " adalah Ibu kota Jawa Tengah");
        } else if (kota == "Surabaya") {
            System.out.println(kota + " adalah Ibu Kota Jawa Timur");
        } else {
            System.out.println(kota);
        }
            
    }
}
